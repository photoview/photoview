import fs from 'fs-extra'
import path from 'path'
import { ApolloServer } from 'apollo-server-express'
import express from 'express'
import bodyParser from 'body-parser'
import cookieParser from 'cookie-parser'
import { v1 as neo4j } from 'neo4j-driver'
import { makeAugmentedSchema } from 'neo4j-graphql-js'
import dotenv from 'dotenv'
import http from 'http'
import PhotoScanner from './scanner/Scanner'
import _ from 'lodash'
import config from './config'

import { getUserFromToken, getTokenFromBearer } from './token'

// set environment variables from ../.env
dotenv.config()

const app = express()
app.use(bodyParser.json())
app.use(cookieParser())

/*
 * Create an executable GraphQL schema object from GraphQL type definitions
 * including autogenerated queries and mutations.
 * Optionally a config object can be included to specify which types to include
 * in generated queries and/or mutations. Read more in the docs:
 * https://grandstack.io/docs/neo4j-graphql-js-api.html#makeaugmentedschemaoptions-graphqlschema
 */

const typeDefs = fs
  .readFileSync(
    process.env.GRAPHQL_SCHEMA || path.join(__dirname, 'schema.graphql')
  )
  .toString('utf-8')

import usersResolver from './resolvers/users'
import scannerResolver from './resolvers/scanner'
import photosResolver from './resolvers/photos'
import { isRawImage } from './scanner/utils'

const resolvers = [usersResolver, scannerResolver, photosResolver]

const schema = makeAugmentedSchema({
  typeDefs,
  config: {
    auth: {
      isAuthenticated: true,
      hasRole: true,
    },
    mutation: false,
    query: false,
    // query: {
    //   exclude: ['ScannerResult', 'AuthorizeResult', 'Subscription'],
    // },
  },
  resolvers: resolvers.reduce((prev, curr) => _.merge(prev, curr), {}),
})

/*
 * Create a Neo4j driver instance to connect to the database
 * using credentials specified as environment variables
 * with fallback to defaults
 */
const driver = neo4j.driver(
  process.env.NEO4J_URI || 'bolt://localhost:7687',
  neo4j.auth.basic(
    process.env.NEO4J_USER || 'neo4j',
    process.env.NEO4J_PASSWORD || 'letmein'
  )
)

const scanner = new PhotoScanner(driver)

setInterval(scanner.scanAll, 1000 * 60 * 60 * 4)

// Specify port and path for GraphQL endpoint
const port = process.env.GRAPHQL_LISTEN_PORT || 4001
const graphPath = '/graphql'

/*
 * Create a new ApolloServer instance, serving the GraphQL schema
 * created using makeAugmentedSchema above and injecting the Neo4j driver
 * instance into the context object so it is available in the
 * generated resolvers to connect to the database.
 */
const server = new ApolloServer({
  context: async function({ req }) {
    let user = null
    let token = null

    if (req && req.headers.authorization) {
      token = getTokenFromBearer(req.headers.authorization)
      user = await getUserFromToken(token, driver)
    }

    return {
      ...req,
      driver,
      scanner,
      user,
      token,
      endpoint: `http://localhost:${port}`,
    }
  },
  schema: schema,
  introspection: true,
  playground: true,
  subscriptions: {
    onConnect: async (connectionParams, webSocket) => {
      const token = getTokenFromBearer(connectionParams.Authorization)
      const user = await getUserFromToken(token, driver)

      return {
        token,
        user,
      }
    },
  },
})

server.applyMiddleware({ app, graphPath })

app.use('/images/:id/:image', async function(req, res) {
  const { id, image } = req.params

  let user = null

  try {
    const token = req.cookies.token
    user = await getUserFromToken(token, driver)
  } catch (err) {
    return res.status(401).send(err.message)
  }

  const session = driver.session()

  const result = await session.run(
    'MATCH (p:Photo { id: {id} })<-[:CONTAINS]-(:Album)<-[:OWNS]-(u:User) RETURN p as photo, u.id as userId',
    {
      id,
    }
  )

  if (result.records.length == 0) {
    return res.status(404).send(`Image not found`)
  }

  const userId = result.records[0].get('userId')
  const photo = result.records[0].get('photo').properties

  if (userId != user.id) {
    return res.status(401).send(`Image not owned by you`)
  }

  session.close()

  let imagePath = path.resolve(config.cachePath, 'images', id, image)

  if (!(await fs.exists(imagePath))) {
    if (image == 'thumbnail.jpg') {
      console.log('Thumbnail not found, generating', photo.path)
      await scanner.processImage(id)

      if (!(await fs.exists(imagePath))) {
        throw new Error('Thumbnail not found after image processing')
      }

      return res.sendFile(imagePath)
    }

    imagePath = photo.path
  }

  if (await isRawImage(imagePath)) {
    console.log('RAW preview image not found, generating', imagePath)
    await scanner.processImage(id)

    imagePath = path.resolve(config.cachePath, 'images', id, image)

    if (!(await fs.exists(imagePath))) {
      throw new Error('RAW preview not found after image processing')
    }

    return res.sendFile(imagePath)
  }

  res.sendFile(imagePath)
})

const httpServer = http.createServer(app)
server.installSubscriptionHandlers(httpServer)

httpServer.listen({ port, graphPath }, () => {
  console.log(
    `ðŸš€ GraphQL endpoint ready at http://localhost:${port}${server.graphqlPath}`
  )
  console.log(
    `ðŸš€ Subscriptions ready at ws://localhost:${port}${server.subscriptionsPath}`
  )
})
